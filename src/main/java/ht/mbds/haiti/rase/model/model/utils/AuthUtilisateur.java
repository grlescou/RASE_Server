package ht.mbds.haiti.rase.model.model.utils;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;
import java.io.Serializable;

/**
 *
 * @author gaetan
 */
public class AuthUtilisateur implements Serializable {
     private String mail;
    private String password;

    public AuthUtilisateur() {
    }

    @JsonCreator
    public AuthUtilisateur(@JsonProperty("mail") String mail, @JsonProperty("password") String password) {
        this.mail = mail;
        this.password = password;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
    
}
